import requests
from decimal import Decimal

# Function to get real-time exchange rates
def get_exchange_rate(base_currency, target_currency):
    # Use a reliable currency exchange API (replace `YOUR_API_KEY` with your actual API key)
    api_url = f"https://v6.exchangerate-api.com/v6/d46a301b5e6ee5e3386b0bcd/latest/{base_currency}"
    
    try:
        response = requests.get(api_url)
        response.raise_for_status()  # Raise an error for bad status codes
        data = response.json()

        # Get the target currency rate
        if target_currency in data['conversion_rates']:
            return Decimal(data['conversion_rates'][target_currency])
        else:
            raise ValueError(f"Target currency {target_currency} not found.")
    except requests.exceptions.RequestException as e:
        print(f"Error fetching exchange rate: {e}")
        return None

# Function to convert currency
def convert_currency(amount, base_currency, target_currency):
    rate = get_exchange_rate(base_currency, target_currency)
    if rate:
        return amount * rate
    else:
        raise ValueError("Unable to fetch the exchange rate.")

